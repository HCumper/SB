    let private questionMark = unicodeGlyphs[0x003F]
    let private space = unicodeGlyphs[0x0020]

    let glyphForCharacter (codePoint: int) (character: char) =
        let resolved =
            match Map.tryFind codePoint qlGlyphs with
            | Some(Unicode unicodeCodePoint) -> Map.tryFind unicodeCodePoint unicodeGlyphs
            | Some(Custom name) -> Map.tryFind name customGlyphs
            | None ->
                let unicodeCodePoint = int character
                Map.tryFind unicodeCodePoint unicodeGlyphs

        resolved
        |> Option.defaultValue (if codePoint < 32 then space else questionMark)

